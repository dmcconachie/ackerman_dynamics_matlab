function play_trajectories(ex1, ex2)

% Iterate through the states, displaying each in turn
max_t = max(size(ex1.y_ref, 2), size(ex2.y_ref, 2));
for idx = 1:max_t
    clf
    plot_env(ex1, ex2);
    
    ex1_idx = min(idx, size(ex1.y_ref, 2));
    ex2_idx = min(idx, size(ex2.y_ref, 2));
    
    subplot(2, 3, 1);
    hold on; 
    plot_car(ex1.slow.x_history(1, ex1_idx), ...
             ex1.slow.x_history(2, ex1_idx), ...
             ex1.slow.x_history(3, ex1_idx), ...
             ex1.slow.x_history(4, ex1_idx), ...
             ex1.l, ex1.w, ex1.slow.color);
    
    subplot(2, 3, 2);
    hold on; 
    plot_car(ex1.med.x_history(1, ex1_idx), ...
             ex1.med.x_history(2, ex1_idx), ...
             ex1.med.x_history(3, ex1_idx), ...
             ex1.med.x_history(4, ex1_idx), ...
             ex1.l, ex1.w, ex1.med.color);
    
    subplot(2, 3, 3);
    hold on; 
    plot_car(ex1.fast.x_history(1, ex1_idx), ...
             ex1.fast.x_history(2, ex1_idx), ...
             ex1.fast.x_history(3, ex1_idx), ...
             ex1.fast.x_history(4, ex1_idx), ...
             ex1.l, ex1.w, ex1.fast.color);
         
    subplot(2, 3, 4);
    hold on; 
    plot_car(ex2.slow.x_history(1, ex2_idx), ...
             ex2.slow.x_history(2, ex2_idx), ...
             ex2.slow.x_history(3, ex2_idx), ...
             ex2.slow.x_history(4, ex2_idx), ...
             ex2.l, ex2.w, ex2.slow.color);
    
    subplot(2, 3, 5);
    hold on; 
    plot_car(ex2.med.x_history(1, ex2_idx), ...
             ex2.med.x_history(2, ex2_idx), ...
             ex2.med.x_history(3, ex2_idx), ...
             ex2.med.x_history(4, ex2_idx), ...
             ex2.l, ex2.w, ex2.med.color);
    
    subplot(2, 3, 6);
    hold on; 
    plot_car(ex2.fast.x_history(1, ex2_idx), ...
             ex2.fast.x_history(2, ex2_idx), ...
             ex2.fast.x_history(3, ex2_idx), ...
             ex2.fast.x_history(4, ex2_idx), ...
             ex2.l, ex2.w, ex2.fast.color);
    
    drawnow;
    pause(0.01);
end

end
